package operators;

public class RelationalOperators {

	public static void main(String[] args) {
	int x =10;
	int y =20;
	if(x==y) {
		System.out.println("Print True");
	} else {
		System.out.println("print False");
	} 
	if(x!=y) {
		System.out.println("Return True");
	} else {
		System.out.println("Return false");
	}
	if(x>=y) {
		System.out.println("print true");
	} else {
		System.out.println("Print false");
	}
	}

}
